			<h1>Consider a Home Inspection When Selling</h1>
			<hr/>
			<p>A home inspection isn’t just for buyers. It’s also something sellers should seriously consider before putting their house on the market.</p>
			<p>Why? Quite simply, you don't know what you don’t know. Imagine getting a great offer on your home only to discover, during the middle of the process, that it needs considerable repairs.</p>
			<p>This is the kind of surprise that can lead to buyers pulling out, costing you time and money on a number of fronts. It’s better to know problems from the start, and either deal with them before listing or price the home accordingly.</p>
			<p>A seller's inspection also provides a reference point from which to compare the findings of the buyer’s inspector. In particular, having a different professional opinion can work to your advantage in price negotiations.</p>

			<h4 class="subheader">Your home inspection is a sales tool
			</h4>
			<p>
				Having your home inspected before listing can also be an effective sales tool. You can confidently say that your home has been pre-inspected and is in tip-top shape, making that much more attractive to buyers.
			</p>
			<h4 class="subheader">
				Attending your home inspection
			</h4>
			<p>Attend your home inspection to see first-hand what the inspector notes, and to learn some important details about the house that may assist you in selling.</p>
			<h4 class="subheader">When attending your inspection:</h5>
			<p>Wear casual clothes and comfortable shoes. You may find yourself crawling under and behind things to see what the inspector is pointing out.</p>
			<p>Plan for the inspection to take two or three hours.</p>
			<p>Feel free to ask questions, but give the inspector time and space to work.</p>

			<div class="panel">
				<h5>Home Inspection Checklist for Sellers:</h5>
				<p>Remove clutter that may get in the way of key inspection areas, such as water heater, furnace and main valves.</p>
				<p>Clean up! A clean house shows you care and that you've maintained the property properly.</p>
				<p>Provide full access to attic, basement, crawl space and garage. Leave the keys if they're needed to enter these areas.</p>
				<p>Provide repair documents. If you've done any remodeling or replaced the roof, furnace or electrical system, provide the paperwork. Also, display any paperwork about new appliances that may be included in the sale.</p>
				<p>Leave utilities connected if your house is vacant.</p>
